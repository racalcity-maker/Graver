function toMonospaceGlyphs(glyphs) {
  const result = {};
  for (const [char, value] of Object.entries(glyphs)) {
    const transformedStrokes = value.strokes.map((stroke) =>
      stroke.map(([x, y]) => [0.14 + (x * 0.72), y])
    );
    result[char] = glyph(transformedStrokes, char === ' ' ? 0.8 : 1.0);
  }
  return result;
}

const FONTS = {
  sans: {
    id: 'sans',
    name: 'Simple Sans',
    defaultAdvance: 1.0,
    spaceAdvance: 0.6,
    glyphs: SANS_GLYPHS,
  },
  mono: {
    id: 'mono',
    name: 'Simple Mono',
    defaultAdvance: 1.0,
    spaceAdvance: 0.8,
    glyphs: toMonospaceGlyphs(SANS_GLYPHS),
  },
};

function resolveFont(family) {
  return FONTS[family] || FONTS.sans;
}

function resolveGlyph(font, char) {
  return font.glyphs[char]
    || font.glyphs[char.toUpperCase()]
    || font.glyphs[char.toLowerCase()]
    || font.glyphs[' '];
}

export function getFontFamilies() {
  return Object.values(FONTS).map((font) => ({
    id: font.id,
    name: font.name,
  }));
}

export function textToStrokeLines(text, options = {}) {
  const family = typeof options === 'string' ? options : options.family;
  const letterSpacing = typeof options === 'number' ? options : options.letterSpacing;
  const font = resolveFont(family);
  const spacing = Math.max(0, Number(letterSpacing) || 0);

  const lines = [];
  let cursor = 0;
  for (const char of String(text || '')) {
    const definition = resolveGlyph(font, char);
    for (const stroke of definition.strokes) {
      lines.push(stroke.map(([x, y]) => [x + cursor, y]));
    }
    const advance = Number.isFinite(definition.advance)
      ? definition.advance
      : (char === ' ' ? font.spaceAdvance : font.defaultAdvance);
    cursor += advance + spacing;
  }

  return {
    lines,
    widthUnits: Math.max(cursor > 0 ? cursor - spacing : 0.8, 0.8),
    heightUnits: 1.35,
  };
}
